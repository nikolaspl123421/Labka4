# Знайти y. Y = 0 якщо 1,2,3,4. Y = 1 якщо 10,11,12. Y = 2 якщо 15,19. Y = 3 в інших випадках
# n - значення змінної
# y - значення, яке приймає змінна згідно умови

n = int(input('Введіть значення змінної n'))
if 1 <= n <= 4:
    y = 0
elif 10 <= n <= 12:
    y = 1
elif n == 15 or n == 19:
    y = 2
else:
    y = 3
print(y)

